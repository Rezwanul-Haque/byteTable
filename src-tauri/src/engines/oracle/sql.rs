//! Pure, driver-free helpers for the Oracle (PL/SQL) adapter: double-quote
//! identifier quoting, `:N`-placeholder WHERE-clause compilation, Oracle type /
//! default mapping, `OFFSET…FETCH` paging, column-DDL generation, and
//! banner-version formatting. **Nothing here depends on the `oracle` crate**, so
//! this whole module — and its unit tests — compiles and runs in the DEFAULT
//! (pure-Rust, no `engine-oracle` feature) build. The live OCI execution lives
//! in `super::driver` (gated behind the feature).
//!
//! # Why `:N`, not `?` or `@P{n}`
//!
//! Oracle bind variables are written `:1, :2, …` in the SQL text and bound
//! left-to-right by position — the analogue of Postgres' `$1` and SQL Server's
//! `@P1`. The rust-oracle driver binds a positional `&[&dyn ToSql]` against these
//! placeholders. So, like the SQL Server adapter, this module threads a running
//! placeholder index.
//!
//! # Dialect specifics honoured here
//!
//! - Identifiers are **double-quoted** (`"NAME"`), doubling an embedded `"`.
//!   Oracle folds unquoted identifiers to UPPERCASE; introspection returns the
//!   uppercase names, and quoting them preserves the exact catalog name.
//! - Paging is `OFFSET n ROWS FETCH NEXT m ROWS ONLY` (12c+).
//! - Booleans are `NUMBER(1)`; identity is `GENERATED BY DEFAULT AS IDENTITY`;
//!   `SYSTIMESTAMP` / `SYS_GUID()` are the default expressions.

use crate::shared::engine::{Condition, FilterOp, FilterSpec, FilterValue, SortSpec};
use crate::shared::error::AppError;

/// JavaScript's `Number.MAX_SAFE_INTEGER` (2^53 − 1). Oracle `NUMBER` values
/// whose magnitude exceeds this serialize as JSON *strings*, matching the other
/// adapters and the [`crate::shared::engine::QueryResult::rows`] contract.
pub const JS_MAX_SAFE_INTEGER: i64 = 9_007_199_254_740_991;

/// The character used in `ESCAPE '\'` for the LIKE family (matches the other
/// adapters). Oracle honours an arbitrary `ESCAPE` character; we state `\`
/// explicitly (Oracle LIKE has no default escape character).
pub const LIKE_ESCAPE: char = '\\';

/// Quote an identifier for interpolation into Oracle SQL: wrap in double quotes,
/// doubling an embedded quote (`"` → `""`). This is Oracle's delimited-identifier
/// rule, the analogue of SQL Server brackets / MySQL backticks. Because Oracle
/// upper-cases unquoted identifiers, the catalog stores names uppercase and this
/// quoting round-trips them exactly.
pub fn quote_ident(ident: &str) -> String {
    format!("\"{}\"", ident.replace('"', "\"\""))
}

/// `"SCHEMA"."TABLE"`, both identifiers double-quoted. Oracle schemas are users;
/// the default schema is the connected user.
pub fn qualified(schema: &str, table: &str) -> String {
    format!("{}.{}", quote_ident(schema), quote_ident(table))
}

// ---------------------------------------------------------------------------
// Type mapping (generic / Postgres-ish → Oracle) — prototype `toOracleType`
// ---------------------------------------------------------------------------

/// Map a generic / Postgres-ish column type to its Oracle equivalent (the
/// prototype's `toOracleType`, structure.jsx). Case-insensitive; unrecognised
/// types (already-Oracle `NUMBER`/`VARCHAR2`/`DATE`/`CLOB`/…) pass through
/// uppercased. Pure — unit-tested without a live server.
pub fn to_oracle_type(input: &str) -> String {
    let t = input.trim().to_ascii_uppercase();
    // Exact matches first.
    match t.as_str() {
        "BOOL" | "BOOLEAN" => return "NUMBER(1)".to_string(),
        "TEXT" => return "CLOB".to_string(),
        "JSON" | "JSONB" => return "CLOB".to_string(),
        "UUID" => return "RAW(16)".to_string(),
        "TIMESTAMPTZ" => return "TIMESTAMP WITH TIME ZONE".to_string(),
        "TIMESTAMP" => return "TIMESTAMP".to_string(),
        "SERIAL" | "BIGSERIAL" => return "NUMBER".to_string(),
        "SMALLINT" => return "NUMBER(5)".to_string(),
        "INT" | "INTEGER" => return "NUMBER(10)".to_string(),
        "BIGINT" => return "NUMBER(19)".to_string(),
        "DOUBLE PRECISION" => return "BINARY_DOUBLE".to_string(),
        "REAL" => return "BINARY_FLOAT".to_string(),
        "BYTEA" => return "BLOB".to_string(),
        _ => {}
    }
    // Parameterised families (keep the `(…)` suffix).
    if let Some(rest) = t.strip_prefix("NUMERIC") {
        return format!("NUMBER{rest}");
    }
    if let Some(rest) = t.strip_prefix("DECIMAL") {
        return format!("NUMBER{rest}");
    }
    // VARCHAR(n) → VARCHAR2(n). Guard against VARCHAR2(n) already (its char after
    // "VARCHAR" is '2', so it does not match the "VARCHAR(" prefix).
    if let Some(rest) = t.strip_prefix("VARCHAR(") {
        return format!("VARCHAR2({rest}");
    }
    // BINARY(n) → RAW(n).
    if let Some(rest) = t.strip_prefix("BINARY(") {
        return format!("RAW({rest}");
    }
    // CHAR(n) and everything else (already-Oracle types) pass through.
    t
}

/// Translate a Postgres-ish default expression to Oracle (the prototype's
/// `toOracleDefault`). `now()`/`CURRENT_TIMESTAMP` → `SYSTIMESTAMP`;
/// `true`/`false` → `1`/`0`; `gen_random_uuid()`/`uuid_generate_v4()` →
/// `SYS_GUID()`; anything else is kept verbatim. Case-insensitive on the known
/// tokens. Pure — unit-tested.
pub fn to_oracle_default(default: &str) -> String {
    let s = default.trim();
    let lower = s.to_ascii_lowercase();
    match lower.as_str() {
        "now()" | "current_timestamp" => "SYSTIMESTAMP".to_string(),
        "true" => "1".to_string(),
        "false" => "0".to_string(),
        "gen_random_uuid()" | "uuid_generate_v4()" => "SYS_GUID()".to_string(),
        _ => s.to_string(),
    }
}

/// Whether a column type (already an Oracle type, or a length-suffixed one)
/// denotes a numeric column — drives the column-insights numeric display / avg.
/// Oracle numbers are `NUMBER`, `FLOAT`, `BINARY_FLOAT`, `BINARY_DOUBLE` (plus
/// the generic aliases the type dropdown may still carry).
pub fn is_numeric_type(data_type: &str) -> bool {
    let base = data_type
        .split('(')
        .next()
        .unwrap_or(data_type)
        .trim()
        .to_ascii_uppercase();
    matches!(
        base.as_str(),
        "NUMBER"
            | "FLOAT"
            | "BINARY_FLOAT"
            | "BINARY_DOUBLE"
            | "NUMERIC"
            | "DECIMAL"
            | "INTEGER"
            | "INT"
            | "SMALLINT"
            | "BIGINT"
    )
}

// ---------------------------------------------------------------------------
// Column DDL generation (prototype `generateDDL` column line, oracle branch)
// ---------------------------------------------------------------------------

/// A single column's Oracle DDL fragment (the body of a `CREATE TABLE`/`ADD`
/// column list), honouring the Oracle-specific ordering the prototype's
/// `generateDDL(..., 'oracle')` produces:
///
/// - the type is mapped via [`to_oracle_type`];
/// - an integer PK becomes an identity column (`GENERATED BY DEFAULT AS
///   IDENTITY`) and takes **no** DEFAULT;
/// - for a non-identity column a DEFAULT (mapped via [`to_oracle_default`])
///   precedes `NOT NULL` (Oracle requires DEFAULT before the null constraint);
/// - `PRIMARY KEY` is inlined for a single-column pk; a non-pk `NOT NULL` is
///   emitted only when the column is not nullable.
///
/// `raw_type` is the incoming (possibly generic) type; `identity` is whether the
/// caller decided this column is an auto-increment integer pk. Pure — tested.
pub fn column_ddl(
    name: &str,
    raw_type: &str,
    pk: bool,
    nullable: bool,
    default: Option<&str>,
    identity: bool,
) -> String {
    let mut line = format!("{} {}", quote_ident(name), to_oracle_type(raw_type));
    if identity {
        line.push_str(" GENERATED BY DEFAULT AS IDENTITY");
    } else if let Some(d) = default.filter(|d| !d.trim().is_empty()) {
        // DEFAULT precedes NOT NULL in Oracle; identity columns take no DEFAULT.
        line.push_str(" DEFAULT ");
        line.push_str(&to_oracle_default(d));
    }
    if pk {
        line.push_str(" PRIMARY KEY");
    } else if !nullable {
        line.push_str(" NOT NULL");
    }
    line
}

/// The `ON DELETE` suffix Oracle accepts for a foreign key: only `CASCADE` and
/// `SET NULL` (Oracle has no `RESTRICT`/`NO ACTION` keyword — that is the default
/// and is emitted by *omitting* the clause). Returns `""` for any other action.
/// Matches the prototype's FK branch.
pub fn fk_on_delete(action: &str) -> &'static str {
    let a = action.trim().to_ascii_uppercase();
    if a.contains("CASCADE") {
        " ON DELETE CASCADE"
    } else if a.contains("SET NULL") {
        " ON DELETE SET NULL"
    } else {
        ""
    }
}

