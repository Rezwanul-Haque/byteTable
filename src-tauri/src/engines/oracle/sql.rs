/// Format the `v$version` banner for the sidebar header. The banner's first line
/// is something like `"Oracle Database 21c Express Edition Release 21.0.0.0.0 -
/// Production"`; we keep the leading `"Oracle Database <edition-ish>"` up to (and
/// including) the version-family token (`19c`/`21c`/`23ai`/…) and drop the
/// trailing "Release … - Production" noise. An empty/odd banner falls back to
/// `"Oracle Database"`.
pub fn display_version(raw: &str) -> String {
    let line = raw.lines().next().unwrap_or(raw).trim();
    if line.is_empty() {
        return "Oracle Database".to_string();
    }
    // Cut at " Release " if present (drops "Release 21.0.0.0.0 - Production").
    let head = match line.find(" Release ") {
        Some(idx) => line[..idx].trim(),
        None => line,
    };
    if head.is_empty() {
        "Oracle Database".to_string()
    } else {
        head.to_string()
    }
}

// ---------------------------------------------------------------------------
// Bulk insert (Oracle `INSERT ALL … SELECT 1 FROM DUAL`)
// ---------------------------------------------------------------------------

/// Build a multi-row insert for M16 bulk generate. Oracle has no multi-`VALUES`
/// syntax, so we emit the `INSERT ALL` form: one `INTO "s"."t" (cols) VALUES
/// (:a, :b)` per row, terminated by `SELECT 1 FROM DUAL`. `n_rows` value groups
/// are emitted; the caller binds `n_rows * columns.len()` values in row-major
/// order against the `:N` placeholders. Pure — unit-tested.
pub fn build_multi_insert_sql(
    schema: &str,
    table: &str,
    columns: &[String],
    n_rows: usize,
) -> String {
    let target = qualified(schema, table);
    let cols_sql = columns
        .iter()
        .map(|c| quote_ident(c))
        .collect::<Vec<_>>()
        .join(", ");
    let mut index = 1usize;
    let mut intos = Vec::with_capacity(n_rows);
    for _ in 0..n_rows {
        let mut placeholders = Vec::with_capacity(columns.len());
        for _ in 0..columns.len() {
            placeholders.push(take_placeholder(&mut index));
        }
        intos.push(format!(
            "INTO {target} ({cols_sql}) VALUES ({})",
            placeholders.join(", ")
        ));
    }
    format!("INSERT ALL {} SELECT 1 FROM DUAL", intos.join(" "))
}

