/// The Oracle 12c+ row-limiting clause for a page: `OFFSET {offset} ROWS FETCH
/// NEXT {limit} ROWS ONLY`. Values are inlined as integers (never user text), so
/// this needs no bind. An `ORDER BY` should precede it for a stable page (the
/// caller adds one); Oracle allows the clause without ORDER BY but the order is
/// then unspecified.
pub fn paging_clause(offset: u64, limit: u64) -> String {
    format!("OFFSET {offset} ROWS FETCH NEXT {limit} ROWS ONLY")
}

