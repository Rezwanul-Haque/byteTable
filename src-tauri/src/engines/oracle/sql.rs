    #[test]
    fn column_ddl_orders_identity_default_and_notnull() {
        // Integer PK → identity, no default, inline PRIMARY KEY.
        assert_eq!(
            column_ddl("ID", "integer", true, false, None, true),
            "\"ID\" NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY"
        );
        // Non-identity column: DEFAULT precedes NOT NULL, and the default maps.
        assert_eq!(
            column_ddl(
                "CREATED_AT",
                "timestamptz",
                false,
                false,
                Some("now()"),
                false
            ),
            "\"CREATED_AT\" TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL"
        );
        // Nullable column with a boolean default (mapped true→1).
        assert_eq!(
            column_ddl("ACTIVE", "boolean", false, true, Some("true"), false),
            "\"ACTIVE\" NUMBER(1) DEFAULT 1"
        );
        // Plain non-null column, no default.
        assert_eq!(
            column_ddl("NAME", "varchar(255)", false, false, None, false),
            "\"NAME\" VARCHAR2(255) NOT NULL"
        );
    }

