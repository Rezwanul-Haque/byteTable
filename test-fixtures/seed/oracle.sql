-- ── Schema objects (object browser: view / matview / function / proc / trigger). ──
-- In a sqlplus script each statement is run by EITHER a trailing `;` OR a lone
-- `/` — never both (both = run twice; the MV's second run hits ORA-00955 and,
-- under WHENEVER SQLERROR EXIT, aborts the rest). Views/MV use `/` only here;
-- the PL/SQL routines below use `/` after their `END;`.
CREATE OR REPLACE VIEW byteshop.active_users AS
  SELECT id, name, email, country FROM byteshop.users WHERE active = 1
/

-- Real materialized view (surfaced under the "Materialized Views" section).
CREATE MATERIALIZED VIEW byteshop.order_totals
  BUILD IMMEDIATE
  REFRESH COMPLETE ON DEMAND AS
  SELECT user_id, COUNT(*) AS orders
  FROM byteshop.orders
  GROUP BY user_id
/

CREATE OR REPLACE FUNCTION byteshop.user_order_count(p_uid IN NUMBER)
RETURN NUMBER IS
  v_count NUMBER;
BEGIN
  SELECT COUNT(*) INTO v_count FROM byteshop.orders WHERE user_id = p_uid;
  RETURN v_count;
END;
/

CREATE OR REPLACE PROCEDURE byteshop.deactivate_user(p_uid IN NUMBER) AS
BEGIN
  UPDATE byteshop.users SET active = 0 WHERE id = p_uid;
END;
/

CREATE OR REPLACE TRIGGER byteshop.orders_touch
BEFORE INSERT ON byteshop.orders
FOR EACH ROW
BEGIN
  NULL; -- no-op audit hook
END;
/

EXIT;
