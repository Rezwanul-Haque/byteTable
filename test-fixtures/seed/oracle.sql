-- Oracle (PL/SQL) seed — run MANUALLY as SYSTEM against the Docker Oracle (the
-- images have no drop-in initdb dir for multi-user setups). The compose service
-- uses `gvenzl/oracle-free:23-slim` (native arm64, PDB `FREEPDB1`). Connect e.g.
--   sqlplus system/ByteTable1!@//localhost:11521/FREEPDB1 @seed/oracle.sql
-- (For an amd64 `gvenzl/oracle-xe` box the PDB is `XEPDB1` instead.)
-- Creates the BYTESHOP / SALES / AUDIT user-schemas (Oracle "schemas are users",
-- uppercase), an e-commerce + audit dataset, and the full object set (view /
-- materialized view / function / procedure / trigger) so the M23 acceptance
-- surfaces have real data: FK hop, structure view, column insights, NUMBER(1)
-- booleans, GENERATED AS IDENTITY, NUMBER money, RAW(16)/BLOB binary, CLOB JSON.
-- Idempotent: drops + recreates the users, so re-running re-seeds cleanly.

SET DEFINE OFF;

-- ── Users = schemas (drop + recreate for a clean re-seed). The DROPs swallow
-- "user does not exist" so a first run is clean; everything after fails loud. ──
BEGIN EXECUTE IMMEDIATE 'DROP USER byteshop CASCADE'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP USER sales CASCADE'; EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP USER "AUDIT" CASCADE'; EXCEPTION WHEN OTHERS THEN NULL; END;
/

WHENEVER SQLERROR EXIT SQL.SQLCODE;

-- AUDIT is an Oracle reserved word, so the audit user MUST be quoted ("AUDIT");
-- Oracle stores it uppercase, and the app quotes identifiers, so it round-trips.
CREATE USER byteshop IDENTIFIED BY "ByteTable1!" QUOTA UNLIMITED ON users;
CREATE USER sales    IDENTIFIED BY "ByteTable1!" QUOTA UNLIMITED ON users;
CREATE USER "AUDIT"  IDENTIFIED BY "ByteTable1!" QUOTA UNLIMITED ON users;
GRANT CREATE SESSION, CREATE TABLE, CREATE VIEW, CREATE MATERIALIZED VIEW,
      CREATE PROCEDURE, CREATE TRIGGER, CREATE SEQUENCE TO byteshop;
GRANT CREATE SESSION, CREATE TABLE TO sales;
GRANT CREATE SESSION, CREATE TABLE TO "AUDIT";

ALTER SESSION SET CURRENT_SCHEMA = BYTESHOP;

-- ── Core tables (BYTESHOP) — identity pks, NUMBER(1) booleans, NUMBER money. ──
CREATE TABLE byteshop.users (
  id         NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name       VARCHAR2(120) NOT NULL,
  email      VARCHAR2(160) UNIQUE,
  country    VARCHAR2(2),
  active     NUMBER(1) DEFAULT 1 NOT NULL,
  created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);
CREATE TABLE byteshop.products (
  id    NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sku   VARCHAR2(32) NOT NULL UNIQUE,
  name  VARCHAR2(120) NOT NULL,
  price NUMBER(10,2) DEFAULT 0 NOT NULL
);
CREATE TABLE byteshop.orders (
  id      NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id NUMBER(10) NOT NULL CONSTRAINT fk_orders_users REFERENCES byteshop.users(id) ON DELETE CASCADE,
  total   NUMBER(10,2),
  status  VARCHAR2(20),
  method  VARCHAR2(20),
  paid    NUMBER(1)
);
CREATE INDEX byteshop.idx_orders_user ON byteshop.orders(user_id);
CREATE TABLE byteshop.order_items (
  id         NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id   NUMBER(10) NOT NULL CONSTRAINT fk_items_orders REFERENCES byteshop.orders(id) ON DELETE CASCADE,
  product_id NUMBER(10) NOT NULL CONSTRAINT fk_items_products REFERENCES byteshop.products(id),
  qty        NUMBER(10) DEFAULT 1 NOT NULL
);

-- ── RAW(16) + CLOB demo (binary cells, JSON viewer, binary FK, SYS_GUID). ──
CREATE TABLE byteshop.accounts (
  id         RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
  handle     VARCHAR2(64) NOT NULL,
  prefs      CLOB,
  avatar     RAW(16),
  created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);
CREATE TABLE byteshop.documents (
  id         RAW(16) DEFAULT SYS_GUID() PRIMARY KEY,
  account_id RAW(16) NOT NULL CONSTRAINT fk_docs_accounts REFERENCES byteshop.accounts(id) ON DELETE CASCADE,
  title      VARCHAR2(160) NOT NULL,
  body       CLOB,
  updated_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);
CREATE INDEX byteshop.idx_documents_account ON byteshop.documents(account_id);

-- ── sales / audit schemas (multi-schema switcher + cross-schema FK hop). ──
-- A cross-schema FK requires the CHILD schema to hold REFERENCES on the parent
-- table (Oracle checks the privilege against the table owner, not the DBA
-- running this) — without it the CREATE fails with a masked ORA-00942.
GRANT REFERENCES ON byteshop.orders TO sales;
GRANT REFERENCES ON byteshop.users  TO "AUDIT";
CREATE TABLE sales.invoices (
  id       NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id NUMBER(10) NOT NULL CONSTRAINT fk_invoices_orders REFERENCES byteshop.orders(id),
  amount   NUMBER(12,2) NOT NULL,
  issued   DATE DEFAULT SYSDATE NOT NULL
);
CREATE TABLE "AUDIT".events (
  id      NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  kind    VARCHAR2(40) NOT NULL,
  user_id NUMBER(10) CONSTRAINT fk_events_users REFERENCES byteshop.users(id),
  ts      TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

